    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $photos = Photos::find($id);
        if (isset($photos)) {
            Photos::disk('public')->delete($photos->photos_page);
            $photos->delete();
        }
        return Redirect::to('/photos/index');
    }
